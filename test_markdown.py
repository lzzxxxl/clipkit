    def extract_title_from_content(self, content):
        patterns = [
            r'原文章标题[：:]\s*([^\n]+?)(?:\s*-->\s*$|\.txt\s*-->\s*$)',
            r'原文件标题[：:]\s*([^\n]+?)(?:\s*-->\s*$|\.txt\s*-->\s*$)',
            r'title[：:]\s*([^\n<]+)',
            r'<title>(.+?)</title>',
        ]
        
        # 添加用户自定义的匹配字段
        if self.match_patterns:
            custom_patterns = self.match_patterns.split(',')
            for pattern in custom_patterns:
                pattern = pattern.strip()
                if pattern:
                    # 构建自定义匹配模式
                    custom_regex = fr'{re.escape(pattern)}[：:]\s*([^\n]+?)'
                    patterns.append(custom_regex)
        
        for pattern in patterns:
            match = re.search(pattern, content, re.MULTILINE | re.IGNORECASE)
            if match:
                title = match.group(1).strip()
                title = re.sub(r'\.txt$', '', title, flags=re.IGNORECASE)
                title = title.strip()
                if title and len(title) > 1:
                    return title
        
        lines = content.split('\n')
        for line in lines[:10]:
            line = line.strip()
            if len(line) > 5 and len(line) < 100 and not line.startswith('```') and not line.startswith('<!--'):
                return line
        
        return None
    
    def is_markdown_content(self, content):
        """检测内容是否为Markdown格式"""
        # 检查常见的Markdown语法特征
        markdown_patterns = [
            r'^#{1,6}\s',  # 标题
            r'^-\s',  # 无序列表
            r'^\d+\.\s',  # 有序列表
            r'\[.+\]\(.+\)',  # 链接
            r'\*\*.+\*\*',  # 粗体
            r'\*.+\*',  # 斜体
            r'```[\s\S]*?```',  # 代码块
            r'>\s',  # 引用
        ]
        
        for pattern in markdown_patterns:
            if re.search(pattern, content, re.MULTILINE):
                return True
        
        return False

